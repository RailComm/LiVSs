"""To aid in making the Translation Files more readable, we provide easy mappings
between the local codes that the back-end uses, and the front end so people 
understand what's going on.

The following was pulled from:
           http://msdn.microsoft.com/en-us/goglobal/bb964664
"""

def ToLanguageString( lcid, defaultCodes=False, default=None ):
    """Returns the local string for the given LCID. If `defaultCodes` is True
    then it will look for the default language string rather than the exact 
    local string. An Example with: lcid = 1033
        language string => 'english'
        local string => 'English - United States'
    Defaults to giving the exact local string. If the lcid doesn't exist in 
    our lists, it will return `default`.
    """
    if type(lcid) is int:
        if defaultCodes:
            return DEFAULT_LCIDS.get( lcid, default )
        else: return LOCAL_CODES.get( lcid, default )
    else: return default

def ToLocalID( langstr ):
    """Checks if the `langstr` is a valid local string, if its not
    present in the exact strings, it will quickly check all the defaults
    for each language. If nothing is found it will return None, otherwise
    it will be the LCID in decimal form.
    """
    for lcid, name in LOCAL_CODES.items():
        if name == langstr: return lcid
    
    low = langstr.lower()
    for lcid, name in DEFAULT_LCIDS.items():
        if name == low: return lcid
    
    return None
    

